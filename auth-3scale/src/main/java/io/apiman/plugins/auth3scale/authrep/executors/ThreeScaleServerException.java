package io.apiman.plugins.auth3scale.authrep.executors;

public class ThreeScaleServerException extends RuntimeException {
    private static final long serialVersionUID = 2031826149004942249L;
    private String errorCode;
    private String errorMessage;

    public ThreeScaleServerException(String message, 
            Exception cause,
            String errorCode,
            String errorMessage) {
        super(message, cause);
        this.errorCode = errorCode;
        this.errorMessage = errorMessage;
    }
    
    public ThreeScaleServerException(String message,
            String errorCode,
            String errorMessage) {
        super(message);
    }

    public String getErrorCode() {
        return errorCode;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

}
